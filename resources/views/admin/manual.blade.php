        </ul>
    </li>

    <li>
        <h1>AS</h1>
        <p>Nesta tela Cadastra-se ou Consulta-se as AS</p>
        <p>Inserir o numero da AS, o caminhao, motorista e ajudante. se for passar a carga pra outro caminhao inserir aqui as informacoes. <br> 
            inserir uma foto das notas que compem a AS localizada em notas fiscais relacionadas.</p>
    </li>
    <li>
        <h1>Notas</h1>
        <p>Nesta tela Cadastra, Consulta ou Encerra as notas das AS</p>
    </li>
    <li>
        <h1>Relatórios</h1>
        <p>Nesta tela obtem-se todos os relatórios do sistema</p>
        <p>
            <li>retorno por motorista - todas as notas retornadas pelo motorista</li>
            <li>AS por motorista - as entregas que esse motorista fez</li>
            <li>AS por Veiculos - as entregas que esse veiculo fez</li>
            <li>usuarios - adm, gestor, mot, ajudante...</li>
            <li>notas - todas as notas do sistema</li>
            <li>Filial - recife, caruaru...</li>
        </p>
    </li>

    <li>
        <h1>Acesso Motorista</h1>
        <p>mostra a AS que esta ativa na sua entrega juntamente com as respectivas notas. cada nota tem acesso a poder da baixa inserindo corretamente as informacoes necessarias.</p>
        <ul>
            <h2>informacoes necessarias</h2>
            <li>é retorno? sim: escrever a observacao sobre o motivo do retorno. nao: pode passar pra proxima informacao.</li>
            <li>qual forma de pagamento? boleto, a vista, bonificacao - marcar a opcao correspondente</li>
            <li>valor? se for bonificacao deixar em branco.</li>
            <li>comprovante? so aparece se nao for retorno. - foto do comprovante da maquina ou pix.</li>
        </ul>
        <p>obs* o motorista deve entrar em contato com o vendedor e com o seu superior responsavel.</p>
    </li>
</ul>



